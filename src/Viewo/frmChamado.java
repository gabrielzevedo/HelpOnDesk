    public int setarChamado(Chamado c){
        int rc=111;
        String sql="SELECT urgencia, descricao, id_Solic, hora, date_format(data, '%d/%m/%Y') from chamado WHERE situacao='aberto' AND id="+c.getId();
        Connection cn=Connecto.Conexao.getConnection();
 //       System.out.println(sql);
        try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            if(rs.next()){
               c.setUrgencia(rs.getString(1));
               c.setDescricao(rs.getString(2));
               c.setIdsolic(rs.getInt(3));
               c.setHora(rs.getString(4));
               c.setData(rs.getString(5));
               
                  cn.close();
                  rc=0;
            }
        }catch (SQLException E){
            System.out.println(E.getMessage());

        }
     
        return rc;
    }
    
