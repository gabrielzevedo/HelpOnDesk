    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        setarNovos();
        int rc=0;
        String sql="UPDATE solicitante SET matricula_Solic= '"+s.getMatricula()+"', nome_Solic= '"+s.getNome()+"', email_Solic='"+s.getEmail()+"', setor_Solic='"+s.getSetor()+"', ramal_Solic="+s.getRamal()+", andar_Solic="+s.getAndar()+" WHERE id="+f.getId();
        String sql2="UPDATE funcionario SET login= '"+f.getLogin()+"', senha= '"+f.getSenha()+"' WHERE id="+f.getId();
        Connection cn= Connecto.Conexao.getConnection();
        try{
            Statement st=cn.createStatement();
            st.executeUpdate(sql);
            st.executeUpdate(sql2);
            JOptionPane.showMessageDialog(null,"Editado com sucesso!");
            this.dispose();
            rc=0;
            cn.close();
        }catch(SQLException  E){
            rc=111;
            JOptionPane.showMessageDialog(null,"Erro ao editar!");
            E.printStackTrace();
        }
    
    }//GEN-LAST:event_btnEditarActionPerformed

